function encodeRRProductId(sku){
    var encodedSku = '';
    var string_array = sku.toLowerCase().split('');
    string_array.forEach(function (entry) {
        if(/[a-zA-Z]/.test(entry)){
            entry = characterToNumber(entry);
        }
        encodedSku += entry;
    });
    return parseInt(encodedSku);
}
